import streamlit as st
import admission  # assuming this is your admission form module

def instruction_page():
    st.set_page_config(page_title="नियम व अटी", page_icon="📘", layout="wide")

    # Show instructions only if form is not triggered
    if "show_admission_form" not in st.session_state:
        st.session_state["show_admission_form"] = False

    if not st.session_state["show_admission_form"]:
        st.subheader("अभ्यासिकेचे फी बाबतचे नियम व अटी")

        rules_text = """
        1) लोणंद अभ्यासिका केंद्राचे सभासद होण्यासाठी प्रवेश फी १०० रु. राहील.  
       (विद्यार्थी सलग २ महिने न आल्यास त्याचा प्रवेश रद्द केला जाईल व पुन्हा प्रवेश घेताना १०० रु. प्रवेश फी भरावी लागेल)
    2) अभ्यासिकेमध्ये बोलताना किंवा कुजबुज करताना कोणी सापडल्यास त्याला त्याच क्षणी अभ्यासिकेमधून बाहेर काढण्यात येईल. अन्यथा १०० रु. दंड आकारण्यात येईल.
    3) दुसऱ्याची चप्पल/वही/पुस्तक परवानगीशिवाय घेतल्यास त्या विद्यार्थ्याला बाहेर काढण्यात येईल किंवा १०० रु. दंड आकारण्यात येईल.
    4) फी भरताना ओळखपत्र आणणे सक्तीचे आहे. अन्यथा १०० रु. दंड आकारला जाईल.
    5) अभ्यासिकेत येताना व जाताना नोंद वहीत नोंद करणे बंधनकारक आहे. नोंद नसल्यास त्या दिवसाचा १०० रु. दंड होईल.
    6) महिन्यात कितीही दिवस उपस्थित असो, पूर्ण महिन्याची फी आकारली जाईल (दि. १५ नंतर प्रवेश घेतल्यास निम्मी फी).
    7) प्रवेश घेतल्यावर दुसऱ्या दिवशी प्रवेश फी व मासिक फी भरावी लागेल, अन्यथा प्रवेश नाकारला जाईल.
    8) अभ्यासिका बंद करायची असल्यास व्यवस्थापकास भेटणे आवश्यक आहे, अन्यथा संपूर्ण महिन्याची फी आकारली जाईल.
    9) फी भरण्याची शेवटची तारीख १० असेल. त्यानंतर प्रवेश रद्द केला जाईल.
    10) अभ्यासिकेच्या आवारात गप्पा मारणे, मोबाईलवर बोलणे, मोठ्याने बोलणे सक्त मनाई आहे.
    11) मौल्यवान वस्तूंची जबाबदारी विद्यार्थ्यांची राहील. अभ्यासिका जबाबदार नाही.
    12) गैरवर्तन किंवा बेशिस्त वर्तन आढळल्यास सभासदत्व रद्द होईल आणि कायदेशीर कारवाई केली जाईल.
    13) अभ्यासिकेतील वस्तूंचे नुकसान केल्यास त्याची भरपाई विद्यार्थ्यांकडून घेतली जाईल.
    14) कोणाचीही जागा फिक्स नाही. जिथे जागा मिळेल तिथे बसावे.
    15) प्रवेश रद्द करावयाचा असल्यास लेखी अर्ज द्यावा.
    16) इतर व्यक्तींना अभ्यासिकेत येण्यास मनाई आहे.
    17) परिसरातील नागरिकांना त्रास होईल असे वर्तन करु नये.
    18) वाहन रस्त्यावर अडथळा होणार नाही याची काळजी घ्यावी.
    19) गच्चीवर फिरताना काळजी घ्यावी. अपघात झाल्यास अभ्यासिका जबाबदार राहणार नाही.
    20) वरील नियम व अटी मी वाचल्या असून त्या मला मान्य आहेत. त्याचा भंग झाल्यास होणाऱ्या कारवाईस मी जबाबदार राहीन.
        """
        st.markdown(rules_text)

        st.divider()

        agree = st.checkbox("मी सर्व नियम व अटी काळजीपूर्वक वाचल्या असून मला त्या मान्य आहेत ✅")

        if agree:
            if st.button("Proceed to Admission Form"):
                st.session_state["show_admission_form"] = True
                st.rerun()  # rerun to refresh and show only the form
        else:
            st.warning("कृपया पुढे जाण्यापूर्वी सर्व नियम व अटी वाचा आणि सहमती दर्शवा.")

    else:
        admission.admission()  # show form only after agreement

# Run page
